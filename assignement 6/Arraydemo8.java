class Arraydemo8
{
   public static void main(String args[])
    {
      String[] words={"my","name","is","karthik"};
      String result=" ";
      for(int i=0;i<words.length;i++)
      {
        result+=words[i];
        
          result+=" ";
         
      }
       System.out.println(result);
    }
}
       
